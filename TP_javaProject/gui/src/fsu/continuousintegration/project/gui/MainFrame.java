    private void onLoadDataMouseClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_onLoadDataMouseClick
        PersonData data = new PersonData();
        ArrayList<Person> results = data.getAll();

        DefaultListModel listModel = new DefaultListModel();
        for (Person person : results) {
            listModel.addElement(person.getFirstName() + " " + person.getLastName());
        }
        listView.setModel(listModel);
    }//GEN-LAST:event_onLoadDataMouseClick

